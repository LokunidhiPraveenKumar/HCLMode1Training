package com.hcl.day1;

public class ExtraType {
	private String name;
	private long runs;

	public void setName(String name) {
		this.name = name;
	}

	public void setRuns(long runs) {
		this.runs = runs;
	}

	public String getName() {
		return this.name;
	}

	public long getRuns() {
		return this.runs;
	}
}
